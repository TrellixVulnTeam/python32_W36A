# 默写
# 递归定义:自己调用自己的过程
	# 递:去  开辟空间
	# 归:回  释放空间
	# 一去一回就是一个递归

# 递归总结
# 1 开辟和释放空间
	# 1 去的过程,每调一次自己,就新开辟一个空间
	# 2 归的过程,不断的释放空间

# 2 递归什么时候触发归的过程
	# 1 程序执行完毕了
	# 2 遇到return了(代表函数的当次调用结束)
		# 第3次调用,将结果返回给第2次调用
		# 第2次调用,将结果返回给第1次调用		

# 3 递归终止条件
	# 否则会不断开辟内存空间,而不释放,就会内存溢出,蓝屏死机

# 4 数据独立不共享
	# 每次新开辟的空间都是独立的















































